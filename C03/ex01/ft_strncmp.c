int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	diff;

	while ((n-- > 0) && (*s1 || *s2))
	{
		diff = (*s1++ - *s2++);
		if (diff)
			return (diff);
	}
	return (0);
}
